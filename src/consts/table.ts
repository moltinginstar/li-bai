type Tree = { [key: string]: Tree };

export const buildPinyinPrefixTree = () => {
  const foriest: Tree = {};
  for (const k in pinyinToArmenian) {
    let trie = foriest;
    for (const c of k) {
      if (!(c in trie)) {
        trie[c] = {};
      }

      trie = trie[c];
    }

    trie[""] = {};
  }

  return foriest;
};
